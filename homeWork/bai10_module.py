import math
import random

a = 4.3
# Lam tron len
print(math.ceil(a))
# Lam tron xuong
print(math.floor(a))

b = 25
# Can bac hai
print(math.sqrt(b))
# Logarit
print(math.log(2.0))
# Ham luy thua
print(math.pow(2.0, 3.0))
# Ham luong giac
print(math.sin(0))
print(math.cos(0))
print(math.tan(45))

# Ví dụ sử dụng random Module:
# Dua ra so bat ky
print(random.random())
# Dua ra so nguyen bat ky trong khoang 1 - 10
print(random.randint(1, 10))
